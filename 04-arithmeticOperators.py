"""
04-arithmeticOperators.py

Beginner's guide to Python arithmetic operators.

Overview:
---------
Arithmetic operators are used to perform mathematical operations on numbers.

Operators and Examples:
-----------------------
"""

# Addition (+)
a = 5 + 3
print(a)  # Output: 8

# Subtraction (-)
b = 10 - 4
print(b)  # Output: 6

# Multiplication (*)
c = 2 * 6
print(c)  # Output: 12

# Division (/)
d = 8 / 2
print(d)  # Output: 4.0

# Floor Division (//) - divides and rounds down to nearest integer
e = 9 // 2
print(e)  # Output: 4

# Modulus (%) - remainder after division
f = 7 % 3
print(f)  # Output: 1

# Exponentiation (**)
g = 2 ** 3
print(g)  # Output: 8

"""
Tips:
-----
- Use these operators with variables or numbers.
- Division `/` always returns a float.
- Floor division `//` returns an integer.
- Modulus `%` is useful for checking even/odd numbers.
"""